export async function POST(request: NextRequest) {
  try {
    const body = await request.json() as { child: Child; situation: SituationInput; isRetry?: boolean }
    const { child, situation, isRetry } = body

    const childProfile = `子どものプロフィール：
名前: ${child.name}
年齢: ${child.age}歳
性別: ${child.gender === 'boy' ? '男の子' : '女の子'}
気質タイプ: ${child.temperament_traits.join('、') || 'なし'}
効いた声かけ: ${child.effective_phrases || 'なし'}
効かない声かけ: ${child.ineffective_phrases || 'なし'}
補足・エピソード: ${child.notes || 'なし'}`

    const situationLines = [
      `今日の状況：`,
      `今の状態: ${situation.current_state}`,
      `直前にやってたこと: ${situation.previous_activity}`,
      situation.parent_state ? `今の親の状態: ${situation.parent_state}` : '',
      situation.trigger ? `イヤイヤのきっかけ: ${situation.trigger}` : '',
      situation.duration ? `続き具合: ${situation.duration}` : '',
      situation.intensity ? `イヤイヤの強さ: ${situation.intensity}` : '',
      situation.additional_notes ? `その他: ${situation.additional_notes}` : '',
      isRetry ? '\n注意：前回の提案はすべて効果がありませんでした。全く異なるアプローチ・切り口で5つの声かけを提案してください。' : '',
    ].filter(Boolean).join('\n')

    const response = await client.messages.create({
      model: 'claude-sonnet-4-6',
      max_tokens: 4096,
      system: [
        {
          type: 'text',
          text: SYSTEM_PROMPT,
          cache_control: { type: 'ephemeral' },
        },
      ],
      messages: [
        {
          role: 'user',
          content: [
            {
              type: 'text',
              text: childProfile,
              cache_control: { type: 'ephemeral' },
            },
            {
              type: 'text',
              text: situationLines,
            },
          ],
        },
      ],
    })

    const content = response.content[0]
    if (content.type !== 'text') {
      throw new Error('Unexpected response type from Claude')
    }

    const jsonText = content.text.trim().replace(/^```(?:json)?\n?/, '').replace(/\n?```$/, '').trim()
    const result = JSON.parse(jsonText)
    return NextResponse.json(result)
  } catch (error) {
    console.error('Claude API error:', error)
    return NextResponse.json({ error: 'Failed to generate suggestions' }, { status: 500 })
  }
}
